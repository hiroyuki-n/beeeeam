<h2>必要な維持費とは？</h2>
<p>維持費と一口に言っても、細かく分けると4つの費用に分類できます。</p>
<figure>
  <img src="/note/server_and_domain/img_2.png">
</figure>
<div class="box">

  <ul>
    <li>（Lv.1）維持費・・・最低限の状態に保つ</li>
    <li>（Lv.2）管理費・・・トラブルに備える</li>
    <li>（Lv.3）更新費・・・情報を発信する</li>
    <li>（Lv.4）販促費・・・アクセスや売上を上げる</li>
  </ul>
</div>

<p>上記のとおり、必要な維持のレベルはホームページに求める目的や役割によって判断できます。</p>
<p>たとえば、初めてホームページを作る方の多くは「会社のことを知ってもらう」を目的にします。いわば「名刺」のような役割のホームページだと、必要な維持のレベルは「更新費」までです。</p>
<p>もし、ホームページに「集客」の役割を期待するのであれば、レベル4の「販促費」までのコストが必要になります。</p>
<p>このように、ホームページの目的や役割によって、必要な維持のレベルを判断してください。</p>

