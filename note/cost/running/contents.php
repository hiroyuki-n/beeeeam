<h3>不具合の対応</h3>
<p>時折、ホームページはバグや不具合が発生します。よくある事例は以下のとおりです。</p>

<div class="box">
  <ul>
    <li>文字化けしている</li>
    <li>デザインが崩れている</li>
    <li>ページの表示速度が極端に遅い</li>
    <li>ページが404エラーと表示される</li>
    <li>お問い合わせフォームが送信されない</li>
  </ul>
</div>



<p>上記のようなバグは、HTMLやCSS、PHPやJavaScriptといった各種言語の記述ミスが原因の可能性があります。</p>

<p>Web制作の知識やスキルを持つ方がいる場合など、自社で対応できれば費用は発生しません。</p>

<p>自社で対応できない場合は、Web制作会社に外注するのが一般的です。費用は作業時間やページごとに「2,000円～30,000円」ほどかかります。</p>

<p>ちなみに、Web制作会社にホームページ制作を依頼した場合、納品から1年間は無料でバグの修正に対応してくれる会社が多いです。</p>

<p>それ以降は、同じく数千円から数万円の費用がかかるので、バグを見つけたら早めに連絡しましょう。</p>

