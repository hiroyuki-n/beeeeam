<h2><b>投資</b>と考える</h2>

<p>ホームページは、目的を達成するための手段です。目的の達成を意識するのであれば、維持費は投資として捉えてください。</p>

<p>なかには、維持費と聞いて<b>極力抑えるべきコスト</b>と認識する方もいるのではないでしょうか。しかし、その考え方は危険です。</p>

<p>ホームページを「家」にたとえてみます。いくら大きくて立派でも、庭に雑草が生えていたり門が錆びていたりすれば、よい印象は抱かないですよね。</p>

<p>ホームページも同じ。規模が小さくても、しっかりと手入れされているほうがユーザーは心地よく訪問できるはずです。</p>

<p>「ビジネスの成長にはホームページが必要。だから費用をかける」と考えれば、ホームページの目的に合わせて必要な維持費を見極められます。</p>


<h2>内訳と相場</h2>


<h3>サーバー</h3>
<p>ホームページを「家」にたとえた場合、サーバー費用はいわゆる「土地代」です。「購入」するか「借りる」かの選択肢があり、レンタルサーバー費用は後者にあたります。</p>
<p>土地がないと家は建てられず、かつ維持できないので、最低限必要な費用です。</p>
<p>費用は安ければ月額1,000円以下、高いレンタルサーバーなら月額数万円から数十万円かかります。</p>
<p>求める機能や性能で選択する必要があるものの、初めてのホームページであれば相場は「1,000円～3,000円」程度で考えてください。</p>
<p>この価格帯のレンタルサーバーは、低価格ながらビジネスに不可欠なセキュリティ対策機能やサポート対応が充実しています。</p>

<h3>ドメイン</h3>
<p>ホームページが<b>家</b>、サーバーが<b>土地</b>ならドメインは<b>住所</b>にあたります。</p>
<p>住所がないと「手紙（電子メール）」は出せず、かつ人も「訪れる（アクセスする）」ことができません。そのため、ドメイン費用も最低限必要です。</p>
